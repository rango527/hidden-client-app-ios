import UIKit


extension UIFont {
    class func avenirBook(_ size: CGFloat) -> UIFont {
        return UIFont(name: Constants.Fonts.Avenir.book, size: size)!
    }
    
    class func avenirRoman(_ size: CGFloat) -> UIFont {
        return UIFont(name: Constants.Fonts.Avenir.roman, size: size)!
    }
    
    class func avenirMedium(_ size: CGFloat) -> UIFont {
        return UIFont(name: Constants.Fonts.Avenir.medium, size: size)!
    }
    
    class func avenirHeavy(_ size: CGFloat) -> UIFont {
        return UIFont(name: Constants.Fonts.Avenir.heavy, size: size)!
    }
    
    class func avenirBlack(_ size: CGFloat) -> UIFont {
        return UIFont(name: Constants.Fonts.Avenir.black, size: size)!
    }
    
    class func avenirOblique(_ size: CGFloat) -> UIFont {
        return UIFont(name: Constants.Fonts.Avenir.oblique, size: size)!
    }
    
    class func avenirMediumOblique(_ size: CGFloat) -> UIFont {
        return UIFont(name: Constants.Fonts.Avenir.mediumOblique, size: size)!
    }

}
